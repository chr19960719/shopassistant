function getip(json) {
    console.log("My IP address is: ", json);
    let val = localStorage.getItem("lang");
    if (val) {
        changLang(val);
        return;
    }
    // 判断是否是国内
    let lang = 'CN';
    if (json.country_code === "CN") {
        localStorage.setItem("lang", "CN");
    } else {
        lang = 'EN';
        localStorage.setItem("lang", "EN");
    }
    changLang(lang);
}
function changLang(lang) {
    $("#lang-switcher").val(lang);
    const all = document.querySelectorAll('[lang]');
    all.forEach((item) => {
        const key = item.getAttribute("lang");
        item.innerHTML = langObj[lang][key] || '';
    });
    const allLangStyle = document.querySelectorAll('[lang-style]');
    allLangStyle.forEach((item) => {
        const key = item.getAttribute("lang-style");
        const className = lang + '_' + key;
        const EN_CLASS = 'EN_' + key;
        const CN_CLASS = 'CN_' + key;
        item.classList.remove(EN_CLASS);
        item.classList.remove(CN_CLASS);
        item.classList.add(className);
    });
}
